package com.jason.lsearch.po;

public class Feature {
	private float centroid;
	private float curvature;
	private float complexity;
	private float graydev;
	private float grayavg;
	private float hu_3;
	private float hueavg;
	private float lpr;
	private float saturationavg;
	private float texture_0;
	
	public Feature() {
		super();
	}
	public float getCentroid() {
		return centroid;
	}
	public void setCentroid(float centroid) {
		this.centroid = centroid;
	}
	public float getCurvature() {
		return curvature;
	}
	public void setCurvature(float curvature) {
		this.curvature = curvature;
	}
	public float getComplexity() {
		return complexity;
	}
	public void setComplexity(float complexity) {
		this.complexity = complexity;
	}
	public float getGraydev() {
		return graydev;
	}
	public void setGraydev(float graydev) {
		this.graydev = graydev;
	}
	public float getGrayavg() {
		return grayavg;
	}
	public void setGrayavg(float grayavg) {
		this.grayavg = grayavg;
	}
	public float getHu_3() {
		return hu_3;
	}
	public void setHu_3(float hu_3) {
		this.hu_3 = hu_3;
	}
	public float getHueavg() {
		return hueavg;
	}
	public void setHueavg(float hueavg) {
		this.hueavg = hueavg;
	}
	public float getLpr() {
		return lpr;
	}
	public void setLpr(float lpr) {
		this.lpr = lpr;
	}
	public float getSaturationavg() {
		return saturationavg;
	}
	public void setSaturationavg(float saturationavg) {
		this.saturationavg = saturationavg;
	}
	public float getTexture_0() {
		return texture_0;
	}
	public void setTexture_0(float texture_0) {
		this.texture_0 = texture_0;
	}
	
	

}
